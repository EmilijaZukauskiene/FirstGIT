package lt.sdacademy.fundamentals.objec_oriental.room;

public class Room {
    private int width;
    private int length;

    public Room(int width, int height) {
        this.width = width;
        this.length = height;
    }

    public int getWidth() {
        return width;
    }

    public int getLength() {
        return length;
    }
}
